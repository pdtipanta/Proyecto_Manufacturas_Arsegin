    private void campo_Busqueda_ReporteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campo_Busqueda_ReporteKeyTyped
        char c = evt.getKeyChar();
        
        if( c == evt.VK_SPACE && this.campo_Busqueda_Reporte.getText().length() == 0 ) evt.consume();
 
        else if(  ( c < 'a' || c > 'z' ) && ( c < 'A' || c>'Z' ) && ( c < '0' || c > '9' ) && ( c < '-' || c > '.' ) && c != evt.VK_SPACE ){
            evt.consume(); 
        }

        if (this.campo_Busqueda_Reporte.getText().length() > 0) {
            this.boton_Generar_Reporte.setEnabled(true);
        } else {
            this.boton_Generar_Reporte.setEnabled(false);
        }
    }//GEN-LAST:event_campo_Busqueda_ReporteKeyTyped

    private void combo_OpcionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_OpcionActionPerformed
        if (evt.getSource() == this.combo_Opcion) {
            if (this.combo_Opcion.getSelectedItem() == "Por codigo") {
                this.campo_Busqueda_Reporte.setText("");
                this.campo_Busqueda_Reporte.setEditable(true);
                this.boton_Generar_Reporte.setEnabled(false);
            } else if (this.combo_Opcion.getSelectedItem() == "Por descripcion") {
                this.campo_Busqueda_Reporte.setText("");
                this.campo_Busqueda_Reporte.setEditable(true);
                this.boton_Generar_Reporte.setEnabled(false);
            } else if (this.combo_Opcion.getSelectedItem() == "Todos") {
                this.campo_Busqueda_Reporte.setText("");
                this.boton_Generar_Reporte.setEnabled(true);
                this.campo_Busqueda_Reporte.setEditable(false);
            } else {
                this.campo_Busqueda_Reporte.setText("");
                this.campo_Busqueda_Reporte.setEditable(false);
                this.boton_Generar_Reporte.setEnabled(false);
            }
        }
    }//GEN-LAST:event_combo_OpcionActionPerformed

