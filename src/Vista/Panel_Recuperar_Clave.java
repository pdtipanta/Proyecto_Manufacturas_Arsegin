     public boolean etiquetas(){
         boolean bandera = true;
         
         if (this.campo_Recuperacion_Usuario.getText().isEmpty()) {
             this.etiqueta_Indicador_Usuario.setVisible(true);
             bandera = false;
         } else {
             this.etiqueta_Indicador_Usuario.setVisible(false);
         }

         if (this.campo_Recuperacion_CI.getText().matches("[0-9][0-9]{9}")) {
             this.etiqueta_Indicador_CI.setVisible(false);
         } else {
             this.etiqueta_Indicador_CI.setVisible(true);
             bandera = false;
         }
         return bandera;
     }
     
     public void activar_Ingreso_Codigo( boolean bandera ){
         this.campo_Recuperacion_Usuario.setEditable( !bandera );
         this.campo_Recuperacion_CI.setEditable( !bandera );
         this.boton_Recuperacion_Recuperar.setEnabled( ! bandera );
         this.campo_Recuperacion_Codigo.setEditable( bandera );
     }
     
     public void activar_Ingreso_Nueva_Clave( boolean bandera){
         this.campo_Recuperacion_Nueva.setEditable( bandera );
         this.campo_Recuperacion_Confirmar.setEditable( bandera );
     }
     
     public boolean nueva_Clave(){
        boolean bandera = true;
        
        if( this.campo_Recuperacion_Nueva.getText().isEmpty() ){ 
            bandera = false;
        }
        
        if( this.campo_Recuperacion_Confirmar.getText().isEmpty() ){ 
            bandera = false;
        }
        return bandera;
     }

