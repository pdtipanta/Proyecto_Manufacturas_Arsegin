    private void campo_Recuperacion_UsuarioKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campo_Recuperacion_UsuarioKeyTyped
        char c = evt.getKeyChar();
        if( c == evt.VK_SPACE && this.campo_Recuperacion_Usuario.getText().length() == 0 ) evt.consume();
        else if( ( c < 'a' || c > 'z' ) && ( c < 'A' || c>'Z' ) && ( c < '0' || c > '9' ) && c != evt.VK_SPACE ){
            evt.consume();
        }
    }//GEN-LAST:event_campo_Recuperacion_UsuarioKeyTyped

