    public boolean etiquetas() {
        boolean bandera = true;
        
        if (this.campo_Nombre_Registro.getText().matches("[A-Z][a-z]+")) {
            this.etiqueta_Nombre.setVisible(false);
        } else {
            this.etiqueta_Nombre.setVisible(true);
            bandera = false;
        }

        if (this.campo_Apellido_Registro.getText().matches("[A-Z][a-z]+")) {
            this.etiqueta_Apellido.setVisible(false);
        } else {
            this.etiqueta_Apellido.setVisible(true);
            bandera = false;
        }

        if (this.campo_Cedula_Registro.getText().matches("[1-9][0-9]{9}")) {
            this.etiqueta_Cedula.setVisible(false);
        } else {
            this.etiqueta_Cedula.setVisible(true);
            bandera = false;
        }

        if (this.campo_Correo_Registro.getText().matches("[a-zA-Z_\\d]+@[a-zA-Z]+(\\.[a-zA-Z]*)+")) {
            this.etiqueta_Correo.setVisible(false);
        } else {
            this.etiqueta_Correo.setVisible(true);
            bandera = false;
        }

        if (this.campo_Uusario_Registro.getText().matches("[a-zA-Z_\\d]+")) {
            this.etiqueta_Usuario.setVisible(false);
        } else {
            this.etiqueta_Usuario.setVisible(true);
            bandera = false;
        }

        if (this.campo_Contraseniaa_Registro.getText().matches("[a-zA-Z_\\d]+")) {
            this.etiqueta_Contraseña.setVisible(false);
        } else {
            this.etiqueta_Contraseña.setVisible(true);
            bandera = false;
        }

        if (this.campo_Recontrasenia_Registro.getText().matches("[a-zA-Z_\\d]+")) {
            this.etiqueta_Repetir_Contraseña.setVisible(false);
        } else {
            this.etiqueta_Repetir_Contraseña.setVisible(true);
            bandera = false;
        }

        if (this.campo_Contraseniaa_Registro.getText().equals(this.campo_Recontrasenia_Registro.getText())) {
            this.etiqueta_Mensaje_Clave.setText("");
        } else {
            this.etiqueta_Mensaje_Clave.setText("Las claves no coinciden");
        }

        if (this.combo_Rol_Registro.getSelectedIndex() != 0) {
            this.etiqueta_Mensaje_Rol.setVisible(false);
        } else {
            this.etiqueta_Mensaje_Rol.setVisible(true);
            bandera = false;
        }
        return bandera;
    }
    
