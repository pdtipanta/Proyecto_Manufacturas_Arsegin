    public void correccion_Campos(String valor){
        if(valor.equals(this.campo_Cedula_Registro.getText())){
            this.etiqueta_Correccion_Cedula.setVisible(true);
        }
        
        if(valor.equals(this.campo_Uusario_Registro.getText())){
            this.etiqueta_Correccion_Usuario.setVisible(true);
        }
    }
    
